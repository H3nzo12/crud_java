    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
       VeiculoDTO vDTO = new VeiculoDTO();
       jfVeiculo jfv = new jfVeiculo();
       
       int linha = jtbVeiculos.getSelectedRow();
        
       vDTO.setId(Integer.parseInt(jtbVeiculos.getModel().getValueAt(linha, 0).toString()));
       vDTO.setPlaca(jtbVeiculos.getModel().getValueAt(linha, 1).toString());
       vDTO.setModelo(jtbVeiculos.getModel().getValueAt(linha, 2).toString()); 
       vDTO.setMarca(jtbVeiculos.getModel().getValueAt(linha, 3).toString());
       vDTO.setKm(Double.parseDouble(jtbVeiculos.getModel().getValueAt(linha, 4).toString()));
       vDTO.setAnoFabri(Integer.parseInt(jtbVeiculos.getModel().getValueAt(linha, 5).toString()));
       vDTO.setAnoModelo(Integer.parseInt(jtbVeiculos.getModel().getValueAt(linha, 6).toString()));
       
       jfv.carregaVeiculo(vDTO);
       jfv.setVisible(true);  
       
       dispose();
    }//GEN-LAST:event_jbAlterarActionPerformed

