    public void listarVeiculos(){
        try {
            VeiculoDAO vDAO = new VeiculoDAO();
            
            DefaultTableModel tabela = (DefaultTableModel) jtbVeiculos.getModel();
            tabela.setNumRows(0);
            
            ArrayList<VeiculoDTO> veiculoc = vDAO.consultar();
            
            
            for(int i=0; i< veiculoc.size(); i++){
                tabela.addRow(new Object[]{
                    veiculoc.get(i).getId(),
                    veiculoc.get(i).getPlaca(),
                    veiculoc.get(i).getModelo(),
                    veiculoc.get(i).getMarca(),
                    veiculoc.get(i).getKm(),
                    veiculoc.get(i).getAnoFabri(),
                    veiculoc.get(i).getAnoModelo()
                    
                });
            }
            JOptionPane.showMessageDialog(null, "OK");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro no listar");
        }
        
    }
